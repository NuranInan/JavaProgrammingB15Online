package day22;

public class MultiplicationTable {
    
    public static void main(String[] args) {




            for (int x = 1; x <=12 ; x++) {

                System.out.println("*****");

                for (int i = 0; i <=12; i++) {


                    System.out.println(x+ "*" + i+"="+ x * i);



                }
        }

       
    }
    
}
