package day21;

public class NumberStairCase {

    public static void main(String[] args) {

        for (int y = 1; y <=5 ; y++) {

            System.out.println("y = " + y);

            for (int x = 0; x <=4 ; x++) {

                System.out.println("x = " + x);

            }


        }


    }
}
